public class DataTypePreSuf {
	public static void main( String[] args ) {

		System.out.println( 0XF );

		System.out.println( 010 );

		System.out.println( 0b1001 );

		int no = 1_000_000;
		System.out.println( no );

		long l = 100L;
		System.out.println( l );

		float f = 98.6f;
		System.out.println( f );

	}
}
